    public GestionSets(Matchtennis m) {
        initComponents();
        
        SetsAdapter setsAdapter = new SetsAdapter(m);
         DefaultTableModel model =(DefaultTableModel) jTable_set.getModel();
         Vector column_name=new Vector();
         Vector data_row=new Vector();        
         for (int i=0 ; i<setsAdapter.getColumnCount();i++){
              column_name.addElement(setsAdapter.getColumnName(i));
              model.setColumnIdentifiers(column_name);
         }  
              for ( int k=0 ; k<setsAdapter.getRowCount();k++)
                   {
                    data_row=new Vector();
                    data_row.addElement(setsAdapter.getValueAt(k,0));
                    data_row.addElement(setsAdapter.getValueAt(k,1));
                    data_row.addElement(setsAdapter.getValueAt(k,2));                   
                       model.addRow(data_row);
                    }
              jTable_set.getColumn("id").setMinWidth(0);
              jTable_set.getColumn("id").setMaxWidth(0);
                       jTable_set.setModel(model); 
                       
    
       
        
        
    }
    
    

