    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
       DefaultTableModel model =(DefaultTableModel) jTable_set.getModel();
        SetTennis setTennis= new SetTennis();
       
       SetTennisDao setTennisDao=new SetTennisDao();
        model.setValueAt((int)jSpinner_score1.getValue(),jTable_set.getSelectedRow() , 1);
        model.setValueAt((int)jSpinner_score2.getValue(),jTable_set.getSelectedRow() , 2);
       setTennis.setScore1((int)jSpinner_score1.getValue());
       setTennis.setScore2((int)jSpinner_score2.getValue());
       setTennis.setId((int) model.getValueAt(jTable_set.getSelectedRow(), 0)); 
       setTennisDao.updateSetTennis(setTennis);
        
    }//GEN-LAST:event_jButton_updateActionPerformed

