        c.setDescription_concours(description.getText());
        c.setFrais_participation(Float.parseFloat(frais.getText()));
        c.setDate_debut_depot(java.sql.Date.valueOf(((JTextField) date_deb.getDateEditor().getUiComponent()).getText()));
        c.setDate_fin_depot(java.sql.Date.valueOf(((JTextField) date_fin.getDateEditor().getUiComponent()).getText()));
        c.setType_concour(type.getText());
        
          
        int id_concour;
        id_concour = (int) listeConcours.get(table_concours.getSelectedRow()).getId_Concour();
