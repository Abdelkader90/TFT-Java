    private void bouton_ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bouton_ajouterActionPerformed
        concoursDAO concours = new concoursDAO();
        Concours c = new Concours();

        if (txt_libelle_concour.getText().equals("")
                | txt_date_concour.getDate() == null
                | site.getText().equals("")
                | description.getText().equals("")
                | frais.getText().equals("")
                | date_deb.getDate() == null
                | date_fin.getDate() == null
                | type.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs");
        } else {
            c.setLibelle_concour(txt_libelle_concour.getText());
            c.setDate_debut_concour(Date.valueOf(((JTextField) txt_date_concour.getDateEditor().getUiComponent()).getText()));
            c.setLieu_concour(site.getText());
            c.setDescription_concours(description.getText());
            c.setFrais_participation(Float.parseFloat(frais.getText()));
            c.setDate_debut_depot(Date.valueOf(((JTextField) date_deb.getDateEditor().getUiComponent()).getText()));
            c.setDate_fin_depot(Date.valueOf(((JTextField) date_fin.getDateEditor().getUiComponent()).getText()));
            c.setType_concour(type.getText());
            
            int rep = JOptionPane.showConfirmDialog(null, "Etes vous sur de vouloir ajouter cet utilisateur?", "Attention", JOptionPane.YES_NO_OPTION);
            if (rep == JOptionPane.YES_OPTION) {
                
            }
            concours.ajouterConcours(c);

            JOptionPane.showMessageDialog(null, "Un concours a été ajouté avec succès");

            DefaultTableModel model = (DefaultTableModel) table_concours.getModel();
            model.addRow(new Object[]{txt_libelle_concour.getText(), Date.valueOf(((JTextField) txt_date_concour.getDateEditor().getUiComponent()).getText()), site.getText(), description.getText(), frais.getText(), Date.valueOf(((JTextField) date_deb.getDateEditor().getUiComponent()).getText()), Date.valueOf(((JTextField) date_fin.getDateEditor().getUiComponent()).getText()), type.getText()});

            txt_libelle_concour.setText("");
            txt_date_concour.setDateFormatString("");
            site.setText("");
            description.setText("");
            frais.setText("");
            date_deb.setDateFormatString("");
            date_fin.setDateFormatString("");
            type.setText("");

            //java.util.Date utilDate = Date.();
            //java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            //u. setDate_naiss(sqlDate);
        }


                                                  }//GEN-LAST:event_bouton_ajouterActionPerformed

