    public void filter(String query) {
        TableRowSorter<MatchAdapter> tr = new TableRowSorter<MatchAdapter>(matchAdapter);
        jTable_match.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }


